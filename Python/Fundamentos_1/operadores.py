# Operadpores Aritmeticos

num1 = 10
num2 = 2

sum = num1 + num2 # Asignarle el resultado de la operacion entre dos variables a otra variable
print(sum)

res = 10 - 2  # Asignarle el resultado de la operacion entre dos numeros a una variable
print(res)

print(num1 * num2)  # Realizar una operacion de dos variables dentro de la funcion print()

print(2 / 5)  # Realizar una operacion de dos numeros dentro de la funcion print()


# Operadores de Asignacion
num1 = num1 + 5
print(num1)

num1 += 5
print(num1)



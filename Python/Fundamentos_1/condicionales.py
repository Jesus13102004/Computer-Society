# Ejemplo de condicional con operadores de comparacion
x = 1
if x > 5:
    print("Mayor que 5")
elif x == 3:
    print("Igual a 3")
else:
    print("Ninguna de las anteriores")


# Ejemplo de condicional con operadores logicos
edad = 20
tiene_credencial = True

if edad >= 18 and tiene_credencial:
    print("Puede entrar")
else:
    print("No puede entrar")

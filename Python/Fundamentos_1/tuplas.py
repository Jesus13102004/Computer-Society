# Declaracion de tuplas

my_tupla = (1, "Hoal", False, 3.9) # Tupla con diferentes tipos de dato
tupla = (5,) # Tupla de un solo elemento, se necesita poner "," para que sea tupla

print(len(my_tupla)) # Numero de elementos en la tupla
print(tupla[0]) # Accede a un indice en especifico de la tupla
print(3.9 in my_tupla) # Verifica si hay un elemento en la tupla